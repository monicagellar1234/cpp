/*To find the swap of two nos*/
#include<iostream>
using namespace std;
int main()
{
    int num1,num2,temp_num;
    cout<<"Enter two nos: ";
    cin>>num1>>num2;
    cout<<"Before swapping:"<<endl<<"num1 = "<<num1<<endl<<"num2 = "<<num2<<endl;
    temp_num=mum1;
    num1=num2;
    num2=temp_num;
    cout<<"After swapping:"<<endl<<"a = "<<num1<<endl<<"b = "<<num2<<endl;
}
